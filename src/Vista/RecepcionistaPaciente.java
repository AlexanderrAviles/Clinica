    public void listar() {
        String sql = "SELECT paciente.id,nombre,apellido_paterno,apellido_materno,telefono,direccion,prevision.prevision,sexo.sexo FROM `paciente` "
                + "RIGHT JOIN prevision on paciente.Prevision_id = prevision.id RIGHT JOIN sexo on paciente.sexo_id = sexo.id;";
        try {
            con = Conexion.getConexion();
            st = con.createStatement();
            rs = st.executeQuery(sql);
            Object[] paciente = new Object[8];
//            String[] Titulos={"ID","DNI","NOMBRES"};         
//            model=new DefaultTableModel(null,Titulos);   
            model = (DefaultTableModel) tblpaciente.getModel();
            while (rs.next() == true) {
                // paciente[0] = rs.getInt("id");
                paciente[0] = rs.getString("id");
                paciente[1] = rs.getString("nombre");
                paciente[2] = rs.getString("apellido_paterno");
                paciente[3] = rs.getString("apellido_materno");
                paciente[4] = rs.getString("sexo");
                paciente[5] = rs.getString("telefono");
                paciente[6] = rs.getString("direccion");
                paciente[7] = rs.getString("prevision");

                model.addRow(paciente);
            }
            tblpaciente.setModel(model);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error Listar: " + e);
        }

    }

