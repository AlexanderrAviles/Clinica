    private void tblpacienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblpacienteMouseClicked
        if (evt.getClickCount() == 2 && !evt.isConsumed()) {
            evt.consume();
            int row = tblpaciente.getSelectedRow();
            if (row == -1) {
                JOptionPane.showMessageDialog(null, "No se Selecciono");
            } else {
                txtRut.setEnabled(false);
                String idd = (String) tblpaciente.getValueAt(row, 0);
                String nombre = (String) tblpaciente.getValueAt(row, 1);
                String apellido_paterno = (String) tblpaciente.getValueAt(row, 2);
                String apellido_materno = (String) tblpaciente.getValueAt(row, 3);
                String sexo = (String) tblpaciente.getValueAt(row, 4);
                String telefono = (String) tblpaciente.getValueAt(row, 5).toString();
                String direccion = (String) tblpaciente.getValueAt(row, 6);
                String prevision = (String) tblpaciente.getValueAt(row, 7);
                txtRut.setText(idd);
                txtNombre.setText(nombre);
                txtApellidoP.setText(apellido_paterno);
                txtApellidoM.setText(apellido_materno);
                comboSexo.setSelectedItem(sexo);
                txtTelefono.setText(telefono);
                txtDireccion.setText(direccion);
                comboPrevision.setSelectedItem(prevision);

            }// TODO add your handling code here:
        } else {
            nuevo();

        }

    }//GEN-LAST:event_tblpacienteMouseClicked

